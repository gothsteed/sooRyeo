package com.sooRyeo.app.dto;

public class TimeDto {
	
	private short day_of_week;
	private short start_period;
	private short end_period;
	
	
	public short getDay_of_week() {
		return day_of_week;
	}
	public void setDay_of_week(Short day_of_week) {
		this.day_of_week = day_of_week;
	}
	public short getStart_period() {
		return start_period;
	}
	public void setStart_period(Short start_period) {
		this.start_period = start_period;
	}
	public short getEnd_period() {
		return end_period;
	}
	public void setEnd_period(Short end_period) {
		this.end_period = end_period;
	}
	
	
	

}
